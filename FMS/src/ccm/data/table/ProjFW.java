package ccm.data.table;

public class ProjFW
{	
	private int pNum;
	private int fNum;
	
	public ProjFW(int pNum, int fNum)
	{
		super();
		this.pNum = pNum;
		this.fNum = fNum;
	}
	
	public int getpNum() { return pNum; }
	public void setpNum(int pNum) { this.pNum = pNum; }
	public int getfNum() { return fNum; }
	public void setfNum(int fNum) { this.fNum = fNum; }
}
